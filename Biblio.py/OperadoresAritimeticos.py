#Função Abs = Retorna um número absoluto
print(abs(-5)) #Saída = 5

#Função Power = Retorna um número elevado a uma potência
print(pow(3,3))  #Saída:  27

#Função Max  = Retorna o maior valor
print(max(3, 5, 7, 9)) #Saída: 9

#Função Min = Retorna o menor valor
print(min(3, 5, 7, 9)) #Saída: 3

#Função Raiz Quadrada = Retorna a raiz quadrada de um número
